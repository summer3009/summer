// è·å–è¯¥è§’è‰²æ‰€æœ‰å¯èƒ½çš„å›å¤æº
function getAllReplySources(npcId, favor) {
    const npc = npcData[npcId];
    if (!npc) return [];
    
    const now = new Date();
    const hour = now.getHours();
    let timePeriod = 'afternoon';
    if (hour >= 5 && hour < 12) timePeriod = 'morning';
    else if (hour >= 12 && hour < 18) timePeriod = 'afternoon';
    else if (hour >= 18 && hour < 22) timePeriod = 'evening';
    else timePeriod = 'night';
    
    let favorLevel = 'low';
    if (favor >= 50) favorLevel = 'high';
    else if (favor >= 20) favorLevel = 'medium';
    
    // æ”¶é›†æ‰€æœ‰å¯èƒ½çš„å›å¤æº
    const allSources = [];
    
    // 1. å‰ä»»ä¸“å±å›å¤ï¼ˆä¼˜å…ˆçº§é«˜ï¼‰
    if (npc.gameState && npc.gameState.ex === true && Math.random() < 0.7) {
        allSources.push(...chatReplies.ex);
    }
    
    // 2. æ‹äººä¸“å±å›å¤ï¼ˆä¼˜å…ˆçº§é«˜ï¼‰
    if (npc.gameState && npc.gameState.love === true && Math.random() < 0.6) {
        allSources.push(...chatReplies.lover);
    }
    
    // 3. è§’è‰²æ€§æ ¼ä¸“å±å›å¤
    if (chatReplies.byPersonality[npcId]) {
        allSources.push(...chatReplies.byPersonality[npcId]);
    }
    
    // 4. æ—¶é—´æ®µå›å¤
    if (chatReplies.byTime[timePeriod]) {
        allSources.push(...chatReplies.byTime[timePeriod]);
    }
    
    // 5. å¥½æ„Ÿåº¦å›å¤
    allSources.push(...chatReplies.byFavor[favorLevel]);
    
    // 6. é€šç”¨å›å¤
    allSources.push(...chatReplies.common);
    
    // å»é™¤é‡å¤é¡¹
    return [...new Set(allSources)];
}

