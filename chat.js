function checkSpecialKeywords(message, npcId, favor) {
    const npc = npcData[npcId];
    if (!npc) return null;
    
    const keywords = chatReplies.specialKeywords;
    let matchedKeyword = null;
    
    // æ£€æŸ¥æ¶ˆæ¯ä¸­æ˜¯å¦åŒ…å«ç‰¹æ®Šå…³é”®è¯
    for (const keyword in keywords) {
        if (message.includes(keyword)) {
            matchedKeyword = keyword;
            break;
        }
    }
    
    if (!matchedKeyword) return null;
    
    const keywordReplies = keywords[matchedKeyword];
    
    // å¦‚æœæ˜¯å¯¹è±¡ç±»å‹ï¼ˆå¦‚"æˆ‘çˆ±ä½ "æœ‰å¤šç§çŠ¶æ€å›å¤ï¼‰
    if (typeof keywordReplies === 'object' && !Array.isArray(keywordReplies)) {
        // æ ¹æ®å…³ç³»çŠ¶æ€é€‰æ‹©å›å¤
        if (npc.gameState.love && keywordReplies.lover) {
            return keywordReplies.lover[Math.floor(Math.random() * keywordReplies.lover.length)];
        } else if (npc.gameState.ex && keywordReplies.ex) {
            return keywordReplies.ex[Math.floor(Math.random() * keywordReplies.ex.length)];
        } else if (favor >= 60 && keywordReplies.highFavor) {
            return keywordReplies.highFavor[Math.floor(Math.random() * keywordReplies.highFavor.length)];
        } else if (keywordReplies.normal) {
            return keywordReplies.normal[Math.floor(Math.random() * keywordReplies.normal.length)];
        }
    }
    
    // å¦‚æœæ˜¯æ•°ç»„ç±»å‹ï¼ˆç›´æ¥è¿”å›éšæœºå›å¤ï¼‰
    if (Array.isArray(keywordReplies)) {
        return keywordReplies[Math.floor(Math.random() * keywordReplies.length)];
    }
    
    return null;
}

